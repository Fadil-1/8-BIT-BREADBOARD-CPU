    ; Hard Reset
    OLR
    NOP

    ; Turns the Display OFF
    OLC 0xAE
    
    ; Sets Column Address
    OLC 0x15
    OLC 0x00 ; Start from 0
    OLC 0x3F ; End at 63(Each column/segment contains two pixels)

    ; Sets Row Address
    OLC 0x75
    OLC 0x0 ; Start from 0
    OLC 0x3F ; End at 63

    ; Sets Contrast Current
    OLC 0x81
    OLC 0x7F ; Max Contrast value

    ; Sets Remap
    OLC 0xA0
    OLC 0b01010010 ; 0x52 Increment pixel cursor from left to right and top to bottom

    ; Sets Display Start Line Register
    OLC 0xA1
    OLC 0x00 ; Start from RAM address 0

    ; Sets Display Offset
    OLC 0xA2
    OLC 0x4b ; Offset value

    ; Sets Display Mode: A4(Totally OFF) A5(Totally ON)
    OLC 0xA4

    ; Sets Full Current Range
    OLC 0x86 ; Maximum current(Lowest divide ratio and highest oscillator frequency)

    ; Sets Clock Divide Ratio and clock frequency
    OLC 0xB3
    OLC 0xF1 ; Max Clock frequency

    ; Sets MUX Ratio for highest frequency
    OLC 0xA8
    OLC 0x3f ; 1/64

    ; Sets Gray Scale Table
    OLC 0xB8

    OLC 0b00000001
    OLC 0b00010001
    OLC 0b00100010
    OLC 0b00110010
    OLC 0b01000011
    OLC 0b01010100
    OLC 0b01100101
    OLC 0b01110110

    ; Draws filled black rectangle(Clears the display)
