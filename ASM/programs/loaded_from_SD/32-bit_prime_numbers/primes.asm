PRIME_DEC_PLACE:
    MOV $A, 0x00
    MOV PRIME_DEC_DIGIT, $A

.PLACE_LOOP:
    MOV $A, PRIME_DEC_B3
    STC
    NOP
    CMP $A, PRIME_DEC_CONST_B3
    JNC .PLACE_DONE
    JZ .CHECK_B2
    JMP .SUB_CONST

.CHECK_B2:
    MOV $A, PRIME_DEC_B2
    STC
    NOP
    CMP $A, PRIME_DEC_CONST_B2
    JNC .PLACE_DONE
    JZ .CHECK_B1
    JMP .SUB_CONST

.CHECK_B1:
    MOV $A, PRIME_DEC_B1
    STC
    NOP
    CMP $A, PRIME_DEC_CONST_B1
    JNC .PLACE_DONE
    JZ .CHECK_B0
    JMP .SUB_CONST

.CHECK_B0:
    MOV $A, PRIME_DEC_B0
    STC
    NOP
    CMP $A, PRIME_DEC_CONST_B0
    JNC .PLACE_DONE

.SUB_CONST:
    MOV $A, PRIME_DEC_B0
    STC
    NOP
    SUB $A, PRIME_DEC_CONST_B0
    MOV PRIME_DEC_B0, $A

    MOV $A, PRIME_DEC_B1
    NOP
    SUB $A, PRIME_DEC_CONST_B1
    MOV PRIME_DEC_B1, $A

    MOV $A, PRIME_DEC_B2
    NOP
    SUB $A, PRIME_DEC_CONST_B2
    MOV PRIME_DEC_B2, $A

    MOV $A, PRIME_DEC_B3
    NOP
    SUB $A, PRIME_DEC_CONST_B3
    MOV PRIME_DEC_B3, $A

    MOV $A, PRIME_DEC_DIGIT
    CLC
    NOP
    ADD $A, 0x01
    MOV PRIME_DEC_DIGIT, $A

    JMP .PLACE_LOOP

.PLACE_DONE:
    MOV $A, PRIME_DEC_DIGIT
    STC
    NOP
    CMP $A, 0x00
    JNZ .PRINT_DIGIT

    MOV $A, PRIME_DEC_STARTED
    STC
    NOP
    CMP $A, 0x00
    JNZ .PRINT_DIGIT

    MOV $A, PRIME_DEC_FORCE
    STC
    NOP
    CMP $A, 0x00
    JNZ .PRINT_DIGIT

    RTS

.PRINT_DIGIT:
    MOV $A, PRIME_DEC_DIGIT
    CLC
    NOP
    ADD $A, 0x30
    MOV PRIME_DEC_CHAR_TMP, $A

    MOV $A, 0x01
    MOV PRIME_DEC_STARTED, $A

    MOV $A, PRIME_DEC_DATA_MODE
    STC
    NOP
    CMP $A, 0x00
    JZ .PRINT_NORMAL

    MOV $A, PRIME_DEC_CHAR_TMP
    JSR PRIME_PUTC
    RTS

.PRINT_NORMAL:
    MOV $A, PRIME_DEC_CHAR_TMP
    JSR OLED5_PUTC
    RTS


