PRIME_CLEAR_DATA_AREA:
    PSH $A
    PSH $B
    PSH $C
    PSH $D

    MOV $D, 0x04

.CLEAR_ROW:
    MOV $C, 0x00

.CLEAR_COL:
    MOV $A, $C
    MOV $B, $D
    JSR OLED5_SET_CURSOR
    JSR OLED5_CLEAR_CURRENT_CELL_DIRECT

    CLC
    NOP
    ADD $C, 0x01

    STC
    NOP
    CMP $C, OLED5_MAX_COLS
    JNZ .CLEAR_COL

    CLC
    NOP
    ADD $D, 0x01

    STC
    NOP
    CMP $D, OLED5_MAX_ROWS
    JNZ .CLEAR_ROW

    PUL $D
    PUL $C
    PUL $B
    PUL $A
    RTS


