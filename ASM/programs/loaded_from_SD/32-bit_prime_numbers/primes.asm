BLINK_CURSOR_AFTER_OK:
    PSH $C

    MOV $C, 0x08

.BLINK_LOOP:
    JSR OLED5_CURSOR_SHOW
    JSR SHOWCASE_DELAY_CURSOR
    JSR OLED5_CURSOR_HIDE
