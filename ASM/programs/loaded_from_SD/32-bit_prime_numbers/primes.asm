PRIME_LOOP:
    MOV $A, OLED_CURSOR_COL
    MOV PRIME_OUT_COL, $A
    MOV $A, OLED_CURSOR_ROW
    MOV PRIME_OUT_ROW, $A

    JSR PRIME_TEST_CURRENT

    MOV $A, PRIME_FLAG
    STC
    NOP
    CMP $A, 0x00
    JZ .NOT_PRIME

    JSR PRIME_INCREMENT_COUNT
    JSR PRIME_UPDATE_COUNT_DISPLAY

    MOV $A, PRIME_OUT_COL
    MOV $B, PRIME_OUT_ROW
    JSR OLED5_SET_CURSOR

    JSR PRIME_PRINT_CANDIDATE_U32_DEC
    JSR PRIME_NEWLINE_AFTER_NUMBER
    JSR SHOWCASE_DELAY_FAST

.NOT_PRIME:
    JSR PRIME_ADVANCE_CANDIDATE
    JMP PRIME_LOOP


PRIME_TEST_CURRENT:
    ; Trial division uses odd divisors. divisor^2 > candidate marks a prime.
    MOV $A, 0x00
    MOV PRIME_FLAG, $A

    MOV $A, PRIME_CAND_B3
    STC
    NOP
    CMP $A, 0x00
    JNZ .SETUP_DIVISOR
    MOV $A, PRIME_CAND_B2
    STC
    NOP
    CMP $A, 0x00
    JNZ .SETUP_DIVISOR
    MOV $A, PRIME_CAND_B1
    STC
    NOP
    CMP $A, 0x00
    JNZ .SETUP_DIVISOR
    MOV $A, PRIME_CAND_B0
    STC
    NOP
    CMP $A, 0x02
    JZ .IS_PRIME

.SETUP_DIVISOR:
    MOV $A, 0x03
    MOV PRIME_DIV_B0, $A
    MOV $A, 0x00
    MOV PRIME_DIV_B1, $A

    MOV $A, 0x09
    MOV PRIME_DIV_SQ_B0, $A
    MOV $A, 0x00
    MOV PRIME_DIV_SQ_B1, $A
    MOV PRIME_DIV_SQ_B2, $A
    MOV PRIME_DIV_SQ_B3, $A

.TEST_LOOP:
    JSR PRIME_CHECK_SQUARE_GT_CANDIDATE
    MOV $A, PRIME_SQ_GT_FLAG
    STC
    NOP
    CMP $A, 0x00
    JNZ .IS_PRIME

    JSR PRIME_MOD_CURRENT_BY_DIV
    MOV $A, PRIME_MOD_ZERO
    STC
    NOP
    CMP $A, 0x00
    JNZ .NOT_PRIME_RESULT

    ; 65535 is the largest divisor needed for a 32-bit candidate.
    MOV $A, PRIME_DIV_B1
    STC
    NOP
    CMP $A, 0xFF
    JNZ .NEXT_DIVISOR
    MOV $A, PRIME_DIV_B0
    STC
    NOP
    CMP $A, 0xFF
    JZ .IS_PRIME

.NEXT_DIVISOR:
    JSR PRIME_UPDATE_DIVISOR_AND_SQUARE
    JMP .TEST_LOOP

.IS_PRIME:
    MOV $A, 0x01
    MOV PRIME_FLAG, $A
    RTS

.NOT_PRIME_RESULT:
    RTS


PRIME_CHECK_SQUARE_GT_CANDIDATE:
    ; Flag becomes 1 when divisor^2 is larger than the current candidate.
    MOV $A, 0x00
    MOV PRIME_SQ_GT_FLAG, $A

    MOV $A, PRIME_CAND_B3
    STC
    NOP
    CMP $A, PRIME_DIV_SQ_B3
    JNC .SQUARE_GT
    JZ .CHECK_B2
    RTS

.CHECK_B2:
    MOV $A, PRIME_CAND_B2
    STC
    NOP
    CMP $A, PRIME_DIV_SQ_B2
    JNC .SQUARE_GT
    JZ .CHECK_B1
    RTS

.CHECK_B1:
    MOV $A, PRIME_CAND_B1
    STC
    NOP
    CMP $A, PRIME_DIV_SQ_B1
    JNC .SQUARE_GT
    JZ .CHECK_B0
    RTS

.CHECK_B0:
    MOV $A, PRIME_CAND_B0
    STC
    NOP
    CMP $A, PRIME_DIV_SQ_B0
    JNC .SQUARE_GT
    RTS

.SQUARE_GT:
    MOV $A, 0x01
    MOV PRIME_SQ_GT_FLAG, $A
    RTS


PRIME_MOD_CURRENT_BY_DIV:
    ; 32-step binary remainder. Divisor is 16-bit, candidate is 32-bit.
    MOV $A, PRIME_CAND_B0
    MOV PRIME_NUM_B0, $A
    MOV $A, PRIME_CAND_B1
    MOV PRIME_NUM_B1, $A
    MOV $A, PRIME_CAND_B2
    MOV PRIME_NUM_B2, $A
    MOV $A, PRIME_CAND_B3
    MOV PRIME_NUM_B3, $A

    MOV $A, 0x00
    MOV PRIME_REM_B0, $A
    MOV PRIME_REM_B1, $A
    MOV PRIME_REM_B2, $A
    MOV PRIME_REM_B3, $A
    MOV PRIME_MOD_ZERO, $A

    MOV $C, 0x20

.MOD_BIT_LOOP:
    CLC
    NOP
    MOV $A, PRIME_NUM_B0
    ADD $A, PRIME_NUM_B0
    MOV PRIME_NUM_B0, $A
    MOV $A, PRIME_NUM_B1
    NOP
    ADD $A, PRIME_NUM_B1
    MOV PRIME_NUM_B1, $A
    MOV $A, PRIME_NUM_B2
    NOP
    ADD $A, PRIME_NUM_B2
    MOV PRIME_NUM_B2, $A
    MOV $A, PRIME_NUM_B3
    NOP
    ADD $A, PRIME_NUM_B3
    MOV PRIME_NUM_B3, $A

    MOV $A, PRIME_REM_B0
    NOP
    ADD $A, PRIME_REM_B0
    MOV PRIME_REM_B0, $A
    MOV $A, PRIME_REM_B1
    NOP
    ADD $A, PRIME_REM_B1
    MOV PRIME_REM_B1, $A
    MOV $A, PRIME_REM_B2
    NOP
    ADD $A, PRIME_REM_B2
    MOV PRIME_REM_B2, $A
    MOV $A, PRIME_REM_B3
    NOP
    ADD $A, PRIME_REM_B3
    MOV PRIME_REM_B3, $A

    JSR PRIME_REM_GE_DIVISOR
    MOV $A, PRIME_REM_GE_FLAG
    STC
    NOP
    CMP $A, 0x00
    JZ .NEXT_BIT

    JSR PRIME_SUB_DIVISOR_FROM_REM

.NEXT_BIT:
    STC
    NOP
    SUB $C, 0x01
    JNZ .MOD_BIT_LOOP

    JSR PRIME_CHECK_REM_ZERO
    RTS


PRIME_REM_GE_DIVISOR:
    MOV $A, 0x00
    MOV PRIME_REM_GE_FLAG, $A

    MOV $A, PRIME_REM_B3
    STC
    NOP
    CMP $A, 0x00
    JNZ .YES
    MOV $A, PRIME_REM_B2
    STC
    NOP
    CMP $A, 0x00
    JNZ .YES

    MOV $A, PRIME_REM_B1
    STC
    NOP
    CMP $A, PRIME_DIV_B1
    JNC .NO
    JZ .CHECK_LOW
    JMP .YES

.CHECK_LOW:
    MOV $A, PRIME_REM_B0
    STC
    NOP
    CMP $A, PRIME_DIV_B0
    JNC .NO

.YES:
    MOV $A, 0x01
    MOV PRIME_REM_GE_FLAG, $A
.NO:
    RTS


PRIME_SUB_DIVISOR_FROM_REM:
    MOV $A, PRIME_REM_B0
    STC
    NOP
    SUB $A, PRIME_DIV_B0
    MOV PRIME_REM_B0, $A

    MOV $A, PRIME_REM_B1
    NOP
    SUB $A, PRIME_DIV_B1
    MOV PRIME_REM_B1, $A

    MOV $A, PRIME_REM_B2
    NOP
    SUB $A, 0x00
    MOV PRIME_REM_B2, $A

    MOV $A, PRIME_REM_B3
    NOP
    SUB $A, 0x00
    MOV PRIME_REM_B3, $A

    RTS


PRIME_CHECK_REM_ZERO:
    MOV $A, 0x00
    MOV PRIME_MOD_ZERO, $A

    MOV $A, PRIME_REM_B0
    STC
    NOP
    CMP $A, 0x00
    JNZ .DONE
    MOV $A, PRIME_REM_B1
    STC
    NOP
    CMP $A, 0x00
    JNZ .DONE
    MOV $A, PRIME_REM_B2
    STC
    NOP
    CMP $A, 0x00
    JNZ .DONE
    MOV $A, PRIME_REM_B3
    STC
    NOP
    CMP $A, 0x00
    JNZ .DONE

    MOV $A, 0x01
    MOV PRIME_MOD_ZERO, $A

.DONE:
    RTS


PRIME_UPDATE_DIVISOR_AND_SQUARE:
    ; (d + 2)^2 = d^2 + 4d + 4.
    JSR PRIME_ADD_DIVISOR_TO_SQUARE
    JSR PRIME_ADD_DIVISOR_TO_SQUARE
    JSR PRIME_ADD_DIVISOR_TO_SQUARE
    JSR PRIME_ADD_DIVISOR_TO_SQUARE
    JSR PRIME_ADD_4_TO_SQUARE

    MOV $A, PRIME_DIV_B0
    CLC
    NOP
    ADD $A, 0x02
    MOV PRIME_DIV_B0, $A

    MOV $A, PRIME_DIV_B1
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_B1, $A

    RTS


PRIME_ADD_DIVISOR_TO_SQUARE:
    MOV $A, PRIME_DIV_SQ_B0
    CLC
    NOP
    ADD $A, PRIME_DIV_B0
    MOV PRIME_DIV_SQ_B0, $A

    MOV $A, PRIME_DIV_SQ_B1
    NOP
    ADD $A, PRIME_DIV_B1
    MOV PRIME_DIV_SQ_B1, $A

    MOV $A, PRIME_DIV_SQ_B2
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_SQ_B2, $A

    MOV $A, PRIME_DIV_SQ_B3
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_SQ_B3, $A

    RTS


PRIME_ADD_4_TO_SQUARE:
    MOV $A, PRIME_DIV_SQ_B0
    CLC
    NOP
    ADD $A, 0x04
    MOV PRIME_DIV_SQ_B0, $A

    MOV $A, PRIME_DIV_SQ_B1
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_SQ_B1, $A

    MOV $A, PRIME_DIV_SQ_B2
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_SQ_B2, $A

    MOV $A, PRIME_DIV_SQ_B3
    NOP
    ADD $A, 0x00
    MOV PRIME_DIV_SQ_B3, $A

    RTS


PRIME_ADVANCE_CANDIDATE:
    ; 2 is followed by odd candidates only: 3, 5, 7, ...
    MOV $A, PRIME_CAND_B3
    STC
    NOP
    CMP $A, 0x00
    JNZ .ADD_TWO
    MOV $A, PRIME_CAND_B2
    STC
    NOP
    CMP $A, 0x00
    JNZ .ADD_TWO
    MOV $A, PRIME_CAND_B1
    STC
    NOP
    CMP $A, 0x00
    JNZ .ADD_TWO
    MOV $A, PRIME_CAND_B0
    STC
    NOP
    CMP $A, 0x02
    JNZ .ADD_TWO

    MOV $A, PRIME_CAND_B0
    CLC
    NOP
    ADD $A, 0x01
    MOV PRIME_CAND_B0, $A
    RTS

.ADD_TWO:
    MOV $A, PRIME_CAND_B0
    CLC
    NOP
    ADD $A, 0x02
    MOV PRIME_CAND_B0, $A

    MOV $A, PRIME_CAND_B1
    NOP
    ADD $A, 0x00
    MOV PRIME_CAND_B1, $A

    MOV $A, PRIME_CAND_B2
    NOP
    ADD $A, 0x00
    MOV PRIME_CAND_B2, $A

    MOV $A, PRIME_CAND_B3
    NOP
    ADD $A, 0x00
    MOV PRIME_CAND_B3, $A

    RTS


PRIME_INCREMENT_COUNT:
    MOV $A, PRIME_COUNT_B0
    CLC
    NOP
    ADD $A, 0x01
    MOV PRIME_COUNT_B0, $A

    MOV $A, PRIME_COUNT_B1
    NOP
    ADD $A, 0x00
    MOV PRIME_COUNT_B1, $A

    MOV $A, PRIME_COUNT_B2
    NOP
    ADD $A, 0x00
    MOV PRIME_COUNT_B2, $A

    MOV $A, PRIME_COUNT_B3
    NOP
    ADD $A, 0x00
    MOV PRIME_COUNT_B3, $A

    RTS


PRIME_UPDATE_COUNT_DISPLAY:
    MOV $A, 0x08
    MOV $B, 0x02
    JSR OLED5_SET_CURSOR

    ; Clear the old count before the new one prints.
    MOV $A, 0x20
    JSR OLED5_PUTC
    MOV $A, 0x20
    JSR OLED5_PUTC
    MOV $A, 0x20
    JSR OLED5_PUTC
